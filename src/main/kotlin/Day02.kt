object Day02 {

    enum class Shape(val score: Int) { Rock(1), Paper(2), Scissors(3) }

    enum class Outcome(val score: Int) { Win(6), Lose(0), Draw(3) }

    data class Game(val me: Shape, val opponent: Shape, val outcome: Outcome) {
        fun score(): Int = me.score + outcome.score
    }

    val part1: (String, String) -> Game = { a, b ->
        val opponent = a.toShape()
        val me = b.toShape()
        Game(me, opponent, me.play(opponent))
    }

    val part2: (String, String) -> Game = { a, b ->
        val opponent = a.toShape()
        val outcome = b.toOutcome()
        Game(myShape(opponent, outcome), opponent, outcome)
    }

    fun games(transform: (String, String) -> Game): List<Game> =
        input.lines().map { it.toGame(transform) }

    fun List<Game>.score(): Int = sumOf { it.score() }

    private fun String.toShape() =
        when (this) {
            "A", "X" -> Rock
            "B", "Y" -> Paper
            "C", "Z" -> Scissors
            else -> error("Unexpected input $this")
        }

    private fun String.toOutcome() =
        when (this) {
            "X" -> Lose
            "Y" -> Draw
            "Z" -> Win
            else -> error("Unexpected input $this")
        }

    private fun Shape.play(opponent: Shape): Outcome =
        when (this) {
            Rock -> when (opponent) {
                Scissors -> Win
                Paper -> Lose
                Rock -> Draw
            }
            Paper -> when (opponent) {
                Rock -> Win
                Scissors -> Lose
                Paper -> Draw
            }
            Scissors -> when (opponent) {
                Paper -> Win
                Rock -> Lose
                Scissors -> Draw
            }
        }

    private fun myShape(opponent: Shape, outcome: Outcome): Shape =
        when (opponent) {
            Rock -> when (outcome) {
                Win -> Paper
                Lose -> Scissors
                Draw -> Rock
            }
            Paper -> when (outcome) {
                Win -> Scissors
                Lose -> Rock
                Draw -> Paper
            }
            Scissors -> when (outcome) {
                Win -> Rock
                Lose -> Paper
                Draw -> Scissors
            }
        }

    private fun String.toGame(transform: (String, String) -> Game): Game =
        split(" ").let { (a, b) -> transform(a, b) }

